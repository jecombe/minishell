#include "libft.h"

int			ft_count_word(char const *s, char c)
{
	int i;
	int sep;

	i = 0;
	sep = 0;
	while (*s != '\0')
	{
		if (sep == 1 && *s == c)
			sep = 0;
		if (sep == 0 && *s != c)
		{
			sep = 1;
			i++;
		}
		s++;
	}
	return (i);
}
